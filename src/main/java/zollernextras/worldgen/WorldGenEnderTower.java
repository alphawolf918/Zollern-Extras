	protected boolean isValidSpawn(World world, BlockPos pos) {
		int i = pos.getX();
		int j = pos.getY();
		int k = pos.getZ();
		
		Block blockBelow = world.getBlockState(new BlockPos(i, j - 1, k))
				.getBlock();
		
		if (blockBelow == Blocks.AIR) {
			return false;
		}
		
		if (blockBelow != Blocks.END_STONE) {
			return false;
		}
		
		return true;
	}
	
